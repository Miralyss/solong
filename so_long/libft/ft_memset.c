#include	<unistd.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (len != 0)
	{
		((unsigned char *)b)[i] = c;
		i++;
		len--;
	}
	return (b);
}
